#include<iostream>
using namespace std;
int main()
{
    // bitwise operators of 2 numbers
    // and &
    // or |
    // Not ~
    // Xor ^
    // left shift << (the no get double)
    // right shift >> (it give divide by 2)

    int a=15 & 30;
    int b=15|30;
    int c=~15;
    int d=15^30;
    int e=15<<2;
    int f=15>>2;
    cout << a << "\n";
    cout << b << "\n";
    cout << c << "\n";
    cout << d << "\n";
    cout << e << "\n";
    cout << f << "\n";
}
